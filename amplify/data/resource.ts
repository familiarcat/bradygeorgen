import { type ClientSchema, a, defineData } from "@aws-amplify/backend";
const schema = a.schema({
  Resume: a.model({ title: a.string(), summaryId: a.id(), summary: a.belongsTo('Summary', 'summaryId'),
    contactInformationId: a.id(), contactInformation: a.belongsTo('ContactInformation', 'contactInformationId'),
    educationId: a.id(), education: a.belongsTo('Education', 'educationId'),
    experienceId: a.id(), experience: a.belongsTo('Experience', 'experienceId'),
    skills: a.hasMany('Skill', 'resumeId') }),
  Summary: a.model({ goals: a.string(), persona: a.string(), url: a.string(), headshot: a.string(),
    gptResponse: a.string(), resume: a.hasOne('Resume', 'summaryId') }),
  ContactInformation: a.model({ name: a.string(), email: a.string(), phone: a.string(),
    resume: a.hasOne('Resume', 'contactInformationId'), references: a.hasMany('Reference', 'contactInformationId') }),
  Reference: a.model({ name: a.string(), phone: a.string(), email: a.string(),
    contactInformationId: a.id(), contactInformation: a.belongsTo('ContactInformation', 'contactInformationId') }),
  Education: a.model({ summary: a.string(), resume: a.hasOne('Resume', 'educationId'),
    schools: a.hasMany('School', 'educationId') }),
  School: a.model({ name: a.string(), educationId: a.id(), education: a.belongsTo('Education', 'educationId'),
    degrees: a.hasMany('Degree', 'schoolId') }),
  Degree: a.model({ major: a.string(), startYear: a.string(), endYear: a.string(),
    schoolId: a.id(), school: a.belongsTo('School', 'schoolId') }),
  Experience: a.model({ title: a.string(), text: a.string(), gptResponse: a.string(),
    resume: a.hasOne('Resume', 'experienceId'), companies: a.hasMany('Company', 'experienceId') }),
  Company: a.model({ name: a.string(), role: a.string(), startDate: a.string(), endDate: a.string(),
    title: a.string(), gptResponse: a.string(), experienceId: a.id(), experience: a.belongsTo('Experience', 'experienceId'),
    skills: a.hasMany('Skill', 'companyId'), engagements: a.hasMany('Engagement', 'companyId'),
    accomplishments: a.hasMany('Accomplishment', 'companyId') }),
  Engagement: a.model({ client: a.string(), startDate: a.string(), endDate: a.string(), gptResponse: a.string(),
    companyId: a.id(), company: a.belongsTo('Company', 'companyId'), accomplishments: a.hasMany('Accomplishment', 'engagementId') }),
  Accomplishment: a.model({ title: a.string(), description: a.string(), link: a.string(),
    companyId: a.id(), company: a.belongsTo('Company', 'companyId'),
    engagementId: a.id(), engagement: a.belongsTo('Engagement', 'engagementId'),
    skills: a.hasMany('Skill', 'accomplishmentId') }),
  Skill: a.model({ title: a.string(), link: a.string(),
    resumeId: a.id(), resume: a.belongsTo('Resume', 'resumeId'),
    companyId: a.id(), company: a.belongsTo('Company', 'companyId'),
    accomplishmentId: a.id(), accomplishment: a.belongsTo('Accomplishment', 'accomplishmentId') })
}).authorization((allow) => [allow.publicApiKey()]);
export type Schema = ClientSchema<typeof schema>;
export const data = defineData({
  schema,
  authorizationModes: { defaultAuthorizationMode: "apiKey", apiKeyAuthorizationMode: { expiresInDays: 30 } },
});
